const formValidation = (bookmark) => {
  const errors = {}
  const urlRegex = /(http(s)?:\/\/.)?(www\.)?[-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b([-a-zA-Z0-9@:%_\+.~#?&//=]*)/g;
  const hexRegex = /^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$/

  if (!bookmark.text) {
    console.log('title required')
    errors.text = 'required'
  }
  if (bookmark.text.length > 12) {
    console.log('title must be less that 12 characters')
    errors.text = 'Must be < 12 characters.'
  }

  if (urlRegex.test(bookmark.url)) {
    console.log('url is valid')
  } else {
    console.log('url is invalid')
    errors.url = 'Invalid URL'
  }

  if (hexRegex.test(bookmark.color) || colorKeywords.includes(bookmark.color)) {
    console.log('color validated')
  } else if (!bookmark.color) {
    console.log('random color assigned')
  } else {
    console.log('invalid color')
    errors.color = 'Invalid color'
  }

  return errors
}

export default formValidation